	// 대입 진행 시 이전에 입력되어 있던 값은 사라진다

	// 변수명 짓는 규칙
	// 1. 변수명은 알파벳, 숫자, 언더바(_)로만 구성
	// 2. 숫자로 시작할 수는 없으며 공백도 넣을 수 없음
