	// 변수 생성 방법

	// 변수의 선언
	// 자료형 변수명; -> 비어 있는 컵을 만듦

	// 변수의 대입
	// 변수명 = 데이터;
	// -> equal의 의미: 우측의 값을 좌측에 대입하겠다는 선언

